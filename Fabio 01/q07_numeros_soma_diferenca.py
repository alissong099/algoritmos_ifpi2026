numero1 = int(input("Digite o primeiro número: "))
numero2 = int(input("Digite o segundo número: "))
numero3 = int(input("Digite o terceiro número: "))

soma_dos_primeiros = numero1 + numero2
diferenca_dos_ultimos = numero2 - numero3

print(f"A soma dos dois primeiros números é {soma_dos_primeiros}, e a diferença dos dois últimos é {diferenca_dos_ultimos}")